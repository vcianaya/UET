            <div class="contenido_cuerpo">
                <center><b><label>UNIDAD EJECUTORA DE TITULACIÓN</br>DEL MINISTERIO DE OBRAS PÚBLICAS, SERVICIOS Y VIVIENDA</label></b></center></br>
                <label><b>Base Legal - Antecedentes</b></label></br></br>
                <label><p class="mi_parrafo">
                    Dentro de los antecedentes históricos con los que cuenta la <b>Unidad Ejecutora de Titulación</b>, corresponde hacer referencia a la trascendencia de las instituciones de vivienda desde su creación, considerando que las principales actividades que efectúa la actual Unidad Ejecutora de Titulación, nacen de actividades y obligaciones pendientes nacientes de las distintas instituciones de vivienda que fueron absorbidas por el <b>Ex Fonvis</b> durante 58 años aproximadamente.
                </p></label>
                <label><p class="mi_parrafo">
                    Mediante Decreto Supremo Nº 04385 de abril de 1956, se instituye el Régimen de Vivienda Popular como un servicio público de carácter social y obligatorio, creándose el Instituto Nacional de Vivienda, incorporándose de manera progresiva a los diferentes sectores de trabajadores de la sociedad civil a fin de cubrir el déficit de vivienda existente en el País.
                </p></label>
                <label><p class="mi_parrafo">
                    Por medio de Decreto supremo Nº 06816 de 3 de julio de 1964,se crea el Consejo Nacional de Vivienda (CONAVI), creándose en los años psoteriores los Consejos Sectoriales de Vivienda (COVIMIN 1970),(CONVIPET 1971), (CONVIFACG 1973), (COVIMA 1973), (CONVICO 1979), (CONVIFRA 1985).
                </p></label>
                <label><p class="mi_parrafo">
                    Mediante Decreto Supremo Nº 21660 de 10 de julio de 1987, se dispone el cierre de los Consejos Sectoriales de Vivienda y creación del FONVI y el Instituto de Vivienda Social (IVS).
                </p></label>
                <label><p class="mi_parrafo">
                    Por Decreto Supremo Nº 23261, de 15 de septiembre de 1992, se crea el FONDO NACIONAL DE VIVIENDA SOCIAL (FONVIS), reemplazando al FONVI y al IVS, que absorbe las labores pendientes de los programas de vivienda social desde la institución del Régimen de Vivienda Popular de 1956. 
                </p></label>
                <label><p class="mi_parrafo">
                    Mediante Decreto Supremo Nº 24935 de 30 de diciembre de 1997, se dispone la liquidación voluntaria del FONVIS y se crea el Programa Nacional de Subsidio de Vivienda PNSV. De esta manera se divide en dos instituciones lo correspondiente al Régimen de Vivienda Social, por un lado el FONVIS que entra en etapa de liquidación, pero sigue con el trabajo operativo de adjudicar y minutar las soluciones habitacionales restantes de las anteriores instituciones de vivienda, y el PNSV, que asume la política de vivienda y posteriormente es sustituido el año 2006 por el PVS (Programa de Vivienda Social), y actualmente por la Agencia Estatal de Vivienda.
                </p></label>
                <label><p class="mi_parrafo">
                    Mediante Decreto Supremo Nº 29001 de 2 de enero de 2007 se creó la Unidad de Titulación del Fondo Nacional de Vivienda Social - FONVIS como entidad desconcentrada, bajo dependencia directa del Ministerio de Obras Públicas, Servicios y Vivienda, cuyo funcionamiento inició el 2 de enero del 2007 y pretendía se concluída el 30 de junio de 2008, teniendo como misión concluir las tareas pendientes del ex FONVIS en liquidación. A través del Decreto Supremo Nº 29617 de 25 de junio de 2008 se dispone, entre otros aspectos, la ampliación de la vigencia de la Unidad de Titulación hasta el 31 de diciembre de 2010. Efectuándose finalmente la última ampliación de la vigencia institucional de la Unidad de Titulación hasta el 31 de diciembre de 2011 por medio de Decreto Supremo Nº 0730 de 8 de diciembre de 2010.
                </p></label>
                <label><p class="mi_parrafo">
                    Que la ley Nº 163 de 8 de agosto de 2011, en el parágrafo II de su artículo 7, dispone que una vez concluída la vigencia de la Unidad de Titulación del Fondo Nacional de Vivienda Social, sus activos y pasivos pasarán a la entidad pública cabeza de sector, así como sus atribuciones derechos y obligaciones.
                </p></label>
                <label><p class="mi_parrafo">
                    Que, por Resolución Ministerial Nº 357 de diciembre de 2011, emitida por el Ministerio de obras Públicas, Servicios y Vivienda, se crea la Unidad Ejecutora de Titulación, con independencia de gestión técnica y operativa y gestión jurídica desconcentrada, bajo dependencia directa del Ministerio de Obras Públicas, Servicios y Vivienda y funcional del Viceministerio de Vivienda y Urbanismo, no estableciendo de manera expresa una vigencia sujeta a plazo.
                </p></label>
                <label><b>Principios</b></label>
                <label><p class="mi_parrafo">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. Gestión Pública por Resultados</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. Horizontabilidad</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. Equidad</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. Justicia</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. Acceso a la Información</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6. Calidad</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7. Eficiencia</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8. Transparencia</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9. Interculturabilidad</br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10. Inclusión y Participación Social
                </p></label>
                <label><b>Misión</b></label>
                <label><p class="mi_parrafo">
                    Desarrollar mecanismos de regualrización de derecho propietario y concluir todas las tareas pendientes del ex FONVIS en liquidación.
                </p></label>
                <label><b>Visión</b></label>
                <label><p class="mi_parrafo">
                    Somos una entidad que con calidad y transparencia ofrece servicios de regularización de derecho propietario de soluciones habitacionales a familias de escasos recursos, recupera cartera en mora, aportes devengados y concluye tareas pendientes del ex FONVIS.
                </p></label>
                <label><b>Objetivos Estratégicos</b></label>
                <label><p class="mi_parrafo">
                    Concluir todas las tareas pendientes del ex FONVIS en Liquidación, para lo cual procesará la documentación e información que permita identificar los procesos judiciales, cartera a recuperar, saneamientos pendientes, minutaciones y otros que pudieran existir.
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="1">
                        Otorgar derecho de propiedad de las soluciones habitacionales de los proyectos habitacionales pertenecientes a adjudicatarios y/o beneficiarios del ex FONVIS en Liquidación.
                    </li></ol>
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="2">
                        Recuperar Bs. 65.000.000,00 por concepto de conciliaciones con Instituciones Financieras Intermediarias, cartera, aportes devengados al régimen de vivienda social
                    </li></ol>
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="3">
                        Evaluar el grado de cumplimiento y eficacia de los sistemas de administración y de los instrumentos de control internos incorporados a ellos, determinar la confiabilidad de los registros y estados financieros y analizar los resultados y eficiencia de las operaciones.
                    </li></ol>
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="4">
                        Patrocinar y asesorar procesos judiciales así como la liquidación del ex FONVIS.
                    </li></ol>
                </p></label>
                <label><b>Objetivos Estratégicos Institucionales</b></label>
                <label><p class="mi_parrafo">
                    Como entidad desconcentrada en proceso de cierre, asume las políticas y directrices del Ministerio de Obras Públicas Servicios y Vivienda.
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="1">
                        <b>Objetivos Estratégicos del Ministerio de Obras Públicas, Servicios y Vivienda</b></br>
                        Somos una entidad que con calidad y transparencia ofrece servicios de regularización de derecho propietario de soluciones habitacionales a familias de escasos recursos, recupera cartera en mora, aportes devengados y concluye tareas pendientes del ex FONVIS.
                    </li></ol>                    
                </p></label>
                <label><p class="mi_parrafo">
                    <ol><li value="2">
                        <b>Objetivos Estratégicos del Viceministerio de Vivienda y Urbanismo</b></br>
                        Contribuir al acceso de la población boliviana a una vivienda y hábitat adecuados, a un costo razonable, basados en la participación, la ayuda mutua, la responsabilidad compartida y la solidaridad social.
                    </li></ol>                    
